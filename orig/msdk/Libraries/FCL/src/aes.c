#define MACRO_ROTR(a,b)   ((a)>>((b)*8)| (a)<<(32-((b)*8)))
#define MACRO_ROTL(a,b)   ((a)<<((b)*8)| (a)>>(32-((b)*8)))

int aes_set_ekey(AES_KEY *key, const u8 *userKey, const int len)
{
  u32 *rk;
  int i = 0;
  u32 temp;
  if (!userKey || !key)
    return -1;
  if (len != 16 && len != 24 && len != 32)
    return -2;
  rk = key->rd_key;
  if (len == 16)
    key->rounds = 10;
  else if (len == 24)
    key->rounds = 12;
  else
    key->rounds = 14;
  rk[0] = GETU32(userKey);
  rk[1] = GETU32(userKey +  4);
  rk[2] = GETU32(userKey +  8);
  rk[3] = GETU32(userKey + 12);
  if (len == 16)
    {
      while (1)
	{
	  temp  = rk[3];
	  rk[4] = rk[0] ^
	    ((u32)te4[(temp >> 16) & 0xff]<<24)^
	    ((u32)te4[(temp >>  8) & 0xff]<<16)^
	    ((u32)te4[(temp) & 0xff]<<8)^
	    ((u32)te4[(temp >> 24)])^
	    rcon[i];
	  rk[5] = rk[1] ^ rk[4];
	  rk[6] = rk[2] ^ rk[5];
	  rk[7] = rk[3] ^ rk[6];
	  if (++i == 10)
	    {
	      return 0;
	    }
	  rk += 4;
	}
    }
  rk[4] = GETU32(userKey + 16);
  rk[5] = GETU32(userKey + 20);
  if (len == 24)
    {
      while (1)
	{
	  temp = rk[ 5];
	  rk[ 6] = rk[ 0] ^
	    ((u32)te4[(temp >> 16) & 0xff]<<24)^
	    ((u32)te4[(temp >>  8) & 0xff]<<16)^
	    ((u32)te4[(temp) & 0xff]<<8)^
	    ((u32)te4[(temp >> 24)])^
	    rcon[i];
	  rk[ 7] = rk[ 1] ^ rk[ 6];
	  rk[ 8] = rk[ 2] ^ rk[ 7];
	  rk[ 9] = rk[ 3] ^ rk[ 8];
	  if (++i == 8)
	    {
	      return 0;
	    }
	  rk[10] = rk[ 4] ^ rk[ 9];
	  rk[11] = rk[ 5] ^ rk[10];
	  rk += 6;
        }
    }
  rk[6] = GETU32(userKey + 24);
  rk[7] = GETU32(userKey + 28);
  if (len == 32)
    {
      while (1)
	{
	  temp = rk[ 7];
	  rk[ 8] = rk[ 0] ^
	    ((u32)te4[(temp >> 16) & 0xff]<<24)^
	    ((u32)te4[(temp >>  8) & 0xff]<<16)^
	    ((u32)te4[(temp) & 0xff]<<8)^
	    ((u32)te4[(temp >> 24)])^
	    rcon[i];
	  rk[ 9] = rk[ 1] ^ rk[ 8];
	  rk[10] = rk[ 2] ^ rk[ 9];
	  rk[11] = rk[ 3] ^ rk[10];
	  if (++i == 7)
	    {
	      return 0;
	    }
	  temp = rk[11];
	  rk[12] = rk[ 4] ^
	    ((u32)te4[(temp >> 24)]<<24)^
	    ((u32)te4[(temp >> 16) & 0xff]<<16)^
	    ((u32)te4[(temp >>  8) & 0xff]<<8)^
	    ((u32)te4[(temp) & 0xff]);
	  rk[13] = rk[ 5] ^ rk[12];
	  rk[14] = rk[ 6] ^ rk[13];
	  rk[15] = rk[ 7] ^ rk[14];
	  rk += 8;
        }
    }
  return 0;
}

int aes_set_dkey(AES_KEY *key, const u8 *userKey, const int len)
{
  u32 *rk;
  int i, j, status;
  u32 temp;
  status = aes_set_ekey(key, userKey, len);
  if (status < 0)
    return status;
  rk = key->rd_key;
  for (i = 0, j = 4 * (key->rounds); i < j; i += 4, j -= 4)
    {
      temp = rk[i    ];
      rk[i    ] = rk[j    ];
      rk[j    ] = temp;
      temp = rk[i + 1];
      rk[i + 1] = rk[j + 1];
      rk[j + 1] = temp;
      temp = rk[i + 2];
      rk[i + 2] = rk[j + 2];
      rk[j + 2] = temp;
      temp = rk[i + 3];
      rk[i + 3] = rk[j + 3];
      rk[j + 3] = temp;
    }
  for (i = 1; i < (key->rounds); i++)
    {
      rk += 4;
      rk[0] =
	Td0[te4[(rk[0] >> 24)]] ^
	MACRO_ROTR(Td0[te4[(rk[0] >> 16) & 0xff]],1) ^
	MACRO_ROTR(Td0[te4[(rk[0] >>  8) & 0xff]],2) ^
	MACRO_ROTR(Td0[te4[(rk[0]) & 0xff]],3);
      rk[1] =
	Td0[te4[(rk[1] >> 24)]] ^
	MACRO_ROTR(Td0[te4[(rk[1] >> 16) & 0xff]],1) ^
	MACRO_ROTR(Td0[te4[(rk[1] >>  8) & 0xff]],2) ^
	MACRO_ROTR(Td0[te4[(rk[1]) & 0xff]],3);
      rk[2] =
	Td0[te4[(rk[2] >> 24)]] ^
	MACRO_ROTR(Td0[te4[(rk[2] >> 16) & 0xff]],1) ^
	MACRO_ROTR(Td0[te4[(rk[2] >>  8) & 0xff]],2) ^
	MACRO_ROTR(Td0[te4[(rk[2]) & 0xff]],3);
      rk[3] =
	Td0[te4[(rk[3] >> 24)]] ^
	MACRO_ROTR(Td0[te4[(rk[3] >> 16) & 0xff]],1) ^
	MACRO_ROTR(Td0[te4[(rk[3] >>  8) & 0xff]],2) ^
	MACRO_ROTR(Td0[te4[(rk[3]) & 0xff]],3);
    }

  return 0;
}

int aes_encrypt(u8 *out, const u8 *in, const AES_KEY *key)
{
  const u32 *rk;
  u32 s0, s1, s2, s3, t0, t1, t2, t3;
  int r;
  rk = key->rd_key;
  s0 = GETU32(in) ^ rk[0];
  s1 = GETU32(in +  4) ^ rk[1];
  s2 = GETU32(in +  8) ^ rk[2];
  s3 = GETU32(in + 12) ^ rk[3];
  r = key->rounds >> 1;
  for (;;)
    {
      t0 =
	Te0[(s0 >> 24)] ^
	MACRO_ROTR(Te0[(s1 >> 16) & 0xff],1) ^
	MACRO_ROTR(Te0[(s2 >>  8) & 0xff],2) ^
	MACRO_ROTR(Te0[(s3) & 0xff],3) ^
	rk[4];
      t1 =
	Te0[(s1 >> 24)] ^
	MACRO_ROTR(Te0[(s2 >> 16) & 0xff],1) ^
	MACRO_ROTR(Te0[(s3 >>  8) & 0xff],2) ^
	MACRO_ROTR(Te0[(s0) & 0xff],3) ^
	rk[5];
      t2 =
	Te0[(s2 >> 24)] ^
	MACRO_ROTR(Te0[(s3 >> 16) & 0xff],1) ^
	MACRO_ROTR(Te0[(s0 >>  8) & 0xff],2) ^
	MACRO_ROTR(Te0[(s1) & 0xff],3) ^
	rk[6];
      t3 =
	Te0[(s3 >> 24)] ^
	MACRO_ROTR(Te0[(s0 >> 16) & 0xff],1) ^
	MACRO_ROTR(Te0[(s1 >>  8) & 0xff],2) ^
	MACRO_ROTR(Te0[(s2) & 0xff],3) ^
	rk[7];
      rk += 8;
      if (--r == 0)
	{
	  break;
	}
      s0 =
	Te0[(t0 >> 24)] ^
	MACRO_ROTR(Te0[(t1 >> 16) & 0xff],1) ^
	MACRO_ROTR(Te0[(t2 >>  8) & 0xff],2) ^
	MACRO_ROTR(Te0[(t3) & 0xff],3) ^
	rk[0];
      s1 =
	Te0[(t1 >> 24)] ^
	MACRO_ROTR(Te0[(t2 >> 16) & 0xff],1) ^
	MACRO_ROTR(Te0[(t3 >>  8) & 0xff],2) ^
	MACRO_ROTR(Te0[(t0) & 0xff],3) ^
	rk[1];
      s2 =
	Te0[(t2 >> 24)] ^
	MACRO_ROTR(Te0[(t3 >> 16) & 0xff],1) ^
	MACRO_ROTR(Te0[(t0 >>  8) & 0xff],2) ^
	MACRO_ROTR(Te0[(t1) & 0xff],3) ^
	rk[2];
      s3 =
	Te0[(t3 >> 24)] ^
	MACRO_ROTR(Te0[(t0 >> 16) & 0xff],1) ^
	MACRO_ROTR(Te0[(t1 >>  8) & 0xff],2) ^
	MACRO_ROTR(Te0[(t2) & 0xff],3) ^
	rk[3];
    }

  s0 =
    ((u32)te4[(t0 >> 24)]<<24) ^
    ((u32)te4[(t1 >> 16) & 0xff]<<16) ^
    ((u32)te4[(t2 >>  8) & 0xff]<<8) ^
    ((u32)te4[(t3) & 0xff]) ^
    rk[0];
  PUTU32(out     , s0);
  s1 =
    ((u32)te4[(t1 >> 24)]<<24) ^
    ((u32)te4[(t2 >> 16) & 0xff]<<16) ^
    ((u32)te4[(t3 >>  8) & 0xff]<<8) ^
    ((u32)te4[(t0) & 0xff]) ^
    rk[1];

  PUTU32(out +  4, s1);

  s2 =
    ((u32)te4[(t2 >> 24)]<<24) ^
    ((u32)te4[(t3 >> 16) & 0xff]<<16) ^
    ((u32)te4[(t0 >>  8) & 0xff]<<8) ^
    ((u32)te4[(t1) & 0xff]) ^
    rk[2];

  PUTU32(out +  8, s2);

  s3 =
    ((u32)te4[(t3 >> 24)]<<24) ^
    ((u32)te4[(t0 >> 16) & 0xff]<<16) ^
    ((u32)te4[(t1 >>  8) & 0xff]<<8) ^
    ((u32)te4[(t2) & 0xff]) ^
    rk[3];

  PUTU32(out + 12, s3);
  return(UCL_OK);
}

